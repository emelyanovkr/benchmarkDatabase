CREATE TABLE executors
(
    executor_id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name        varchar(256)
);

CREATE TABLE flows
(
    flow_id     bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    state       varchar,
    executor_id bigint REFERENCES executors (executor_id),
    status      int
);

CREATE INDEX status_id_idx ON flows USING btree (status);